# Python-скрипт для обработки данных и генерации SQL

def process_data(data):
    # Шаблон SQL для генерации запросов
    sql_template = ("begin try insert into Address (IDAddress, IDTypeAddress, IDPlace, IDStreet, IDHouse, Flat) "
                    "values ({}, {}, {}, {}, {}, {}) end try begin catch print error_message() end catch;")

    # Генерация SQL-скрипта
    sql_script = "\n".join([sql_template.format(*row) for row in data])
    return sql_script


def save_script_to_file(script, file_name="generated_insert_script.sql"):
    with open(file_name, "w") as file:
        file.write(script)
    print(f"SQL-скрипт сохранен в файл: {file_name}")


